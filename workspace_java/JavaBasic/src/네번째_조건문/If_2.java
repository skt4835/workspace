package 변수와자료형.네번째_조건문;

public class If_2 {
    public static void main(String[] args) {
        System.out.println("프로그램 시작");

        int x = 6;

        //x의 값이 7이면 '1' 출력
        //그렇지 않으면 '2'를 출력

        if (x == 7) {
            System.out.println(1);
        }
        else {
            System.out.println(2);
        }

        System.out.println("프로그램 종료");
    }
}
